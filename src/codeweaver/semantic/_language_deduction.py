def deduce_from_node_types(node_types: Sequence[LiteralStringT]) -> SemanticSearchLanguage | None:
    """Deduces the programming language from a sequence of node types.

    Args:
        node_types: A sequence of node type strings.

    Returns:
        The deduced programming language, or None if it cannot be determined.
    """
    if key := next((node for node in node_types if node in UNIQUE_NODES), None):
        return UNIQUE_NODES[cast(LiteralStringT, key)]
    return None


type NodeInfoKey = Literal["children", "extra", "fields", "root", "subtypes", "named", "type"]


def _determine_intersection(
    field: tuple[NodeInfoKey, ...], possible_languages: set[SemanticSearchLanguage]
) -> set[SemanticSearchLanguage]:
    """Determines the intersection of possible languages based on the observed field names."""
    field = tuple(sorted(f for f in field if f not in ("type", "named")))
    if not field:
        return possible_languages
    match field:
        case ("fields",):
            return possible_languages.intersection(SIMPLE_FIELD_WITH_FIELDS_LANGS)
        case ("children", "fields"):
            return possible_languages.intersection(SIMPLE_FIELD_WITH_CHILDREN_LANGS)
        case ("extra",):
            return possible_languages.intersection(EXTRA_FIELD_LANGS)
        case ("extra", "fields"):
            return possible_languages.intersection(EXTRA_FIELD_WITH_FIELDS_LANGS)
        case ("children", "extra", "fields"):
            return possible_languages.intersection(EXTRA_FIELD_WITH_CHILDREN_LANGS)
        case ("children", "fields", "root"):
            return possible_languages.intersection(ROOT_FIELD_LANGS)
        case ("subtypes",):
            return possible_languages.intersection(SUBTYPE_FIELD_LANGS)
        case _:
            pass
    return possible_languages


