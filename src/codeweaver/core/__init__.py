"""
Core package for CodeWeaver.

This package contains the core functionality and types used throughout the CodeWeaver project. It consists of:

- Foundational data structures like `DiscoveredFile`, `Span`, `CodeChunk` (maybe the most important class in the project!), and various metadata types.
- The UUIDStore and BlakeStore implementations, which are used throughout the project for storing, caching, and deduplicating data.
- Core and base types such as `BasedModel`, `BaseEnum`, and various type aliases for consistent typing across the project.
- Exception classes for handling errors specific to CodeWeaver operations.
- Extensive utilities for common tasks like file handling, hashing, configuration management, and environment detection.
- Logging configuration and setup utilities to standardize logging across the project.
- **A lot** of other types for various purposes, including statistics tracking, provider management, serialization/deserialization, search, and more.

**About all of the imports:**

As a new project, I'm honestly not sure what to expose or not expose yet -- I don't have a good grasp of how people will use this package.

Above all, I want CodeWeaver to be easy to work with for everyone, and I know how frustrating it can be to try to dig up some deeply buried type that you need for type checking. So for now, I'm exposing everything in the core package's `__init__.py` by lazy imports, and will also do this for every other package.

This doesn't mean this is the public API. I've been pretty clear that while we're in release-candidate (alpha/beta) phase, there **is no public API**. As the project matures and stabilizes, we'll look to lock down a reasonable public API, probably while still exposing most of the types for convenience and IDE support.

Is this too much?  Yes.  Will it cause issues down the line?  Probably.
"""

